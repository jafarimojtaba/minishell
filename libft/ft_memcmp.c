#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	size_t			i;
	unsigned char	*s11;
	unsigned char	*s22;

	i = 0;
	s11 = (unsigned char *)s1;
	s22 = (unsigned char *)s2;
	if (n == 0)
		return (0);
	while (i < n && s11[i] == s22[i])
	{
		i++;
	}
	if (i == n)
		i--;
	return ((int)(s11[i] - s22[i]));
}
